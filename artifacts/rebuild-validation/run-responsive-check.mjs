import { chromium } from 'playwright';
import fs from 'fs/promises';
import path from 'path';
const root=process.cwd();
const pages=['index.html','dashboard.html','projects.html','proof.html','resume.html','contact.html','evidence-library/index.html','evidence-library/preserved-sharepoint/index.html'];
const widths=[1920,1440,1280,768,430,390];
const out=path.join(root,'artifacts','rebuild-validation','screenshots');
await fs.mkdir(out,{recursive:true});
const browser=await chromium.launch({headless:true, executablePath:'C:\\Program Files\\Google\\Chrome\\Application\\chrome.exe', args:['--no-sandbox','--disable-dev-shm-usage','--disable-web-security','--allow-file-access-from-files']});
const rows=[];
for (const pageRel of pages){
  for (const width of widths){
    const page=await browser.newPage({viewport:{width,height:1000}, deviceScaleFactor:1});
    const url='http://127.0.0.1:8765/'+pageRel;
    await page.goto(url,{waitUntil:'load'});
    await page.waitForTimeout(250);
    const metrics=await page.evaluate(()=>({
      scrollWidth:document.documentElement.scrollWidth,
      clientWidth:document.documentElement.clientWidth,
      bodyScrollWidth:document.body.scrollWidth,
      links:[...document.querySelectorAll('a')].length,
      h1:[...document.querySelectorAll('h1')].map(x=>x.innerText.trim()).join(' | '),
      navOpen:!!document.querySelector('.nav-links')
    }));
    const overflow = Math.max(metrics.scrollWidth,metrics.bodyScrollWidth) > metrics.clientWidth + 2;
    const shot=`${pageRel.replaceAll('/','__').replace('.html','')}-${width}.png`;
    await page.screenshot({path:path.join(out,shot), fullPage:true});
    rows.push({page:pageRel,width,overflow,scrollWidth:metrics.scrollWidth,clientWidth:metrics.clientWidth,screenshot:`artifacts/rebuild-validation/screenshots/${shot}`,links:metrics.links,h1:metrics.h1});
    await page.close();
  }
}
await browser.close();
let md='# Responsive Screenshot Report\n\nGenerated by Playwright from rebuilt local files.\n\n';
md += `- Pages checked: ${pages.length}\n- Widths checked: ${widths.join(', ')}\n- Screenshots captured: ${rows.length}\n- Overflow failures: ${rows.filter(r=>r.overflow).length}\n\n`;
md += '| Page | Width | Overflow | Scroll width | Client width | Screenshot |\n|---|---:|---|---:|---:|---|\n';
for (const r of rows) md += `| ${r.page} | ${r.width} | ${r.overflow?'FAIL':'PASS'} | ${r.scrollWidth} | ${r.clientWidth} | ${r.screenshot} |\n`;
await fs.writeFile(path.join(root,'artifacts','rebuild-validation','RESPONSIVE-SCREENSHOT-REPORT.md'),md);
await fs.writeFile(path.join(root,'artifacts','rebuild-validation','responsive-results.json'),JSON.stringify(rows,null,2));
console.log(JSON.stringify({screenshots:rows.length,overflowFailures:rows.filter(r=>r.overflow).length},null,2));
